#include "Headers/stack.h" // Подключение заголовочного файла, содержащего определения и операции для работы со стеком

/**
 * Функция `push` добавляет значение типа double в стек, если не достигнут лимит размера стека.
 * 
 * @param stack Параметр `stack` является указателем на структуру типа `Stack`. Эта структура
 * вероятно содержит массив `items` для хранения элементов стека, целое число `top`, чтобы отслеживать
 * верхний элемент стека, и, возможно, другие члены, связанные с операциями со стеком.
 * @param value Параметр `value` в функции `push` представляет собой значение типа double, которое вы хотите
 * добавить в стек. При вызове функции `push` вы предоставляете конкретное значение типа double, которое
 * хотите поместить в стек.
 */
// Операции со стеком для типа double
void push(Stack *stack, double value) { // Функция для добавления значения в стек
    if (stack->top < MAX_STACK_SIZE - 1) { // Проверка, не превышен ли размер стека
        stack->items[++stack->top] = value; // Увеличиваем верхушку стека и добавляем значение
    }
}

/**
 * Функция `pop` извлекает и возвращает верхний элемент из стека, если он не пуст, в противном случае
 * возвращает 0.0.
 * 
 * @param stack Параметр `stack` в функции `pop` является указателем на структуру, представляющую стек.
 * Структура, вероятно, содержит массив `items` для хранения элементов стека и целое число `top`, чтобы
 * отслеживать верхний элемент в стеке.
 * 
 * @return Функция `pop` возвращает верхний элемент стека, если стек не пуст. Если стек пуст, возвращает 0.0.
 */
double pop(Stack *stack) { // Функция для извлечения значения из стека
    if (stack->top >= 0) { // Проверка, не пуст ли стек
        return stack->items[stack->top--]; // Возвращаем верхний элемент и уменьшаем верхушку стека
    }
    return 0.0; // Если стек пуст, возвращаем 0.0
}

/**
 * Функция `pushString` добавляет строку в стек, если не превышен лимит размера стека.
 * 
 * @param stack Параметр `stack` является указателем на структуру `StringStack`, которая вероятно содержит
 * массив строк (`items`), целое число, представляющее верхушку стека (`top`), и, возможно, постоянную
 * `MAX_STACK_SIZE`, указывающую максимальный размер стека.
 * @param value Параметр `value` в функции `pushString` представляет строку, которую вы хотите
 * добавить в стек. Это указатель на массив символов (`char *value`), содержащий
 * символы строки, которую вы хотите поместить в стек.
 */
// Операции со стеком для строк
void pushString(StringStack *stack, char *value) { // Функция для добавления строки в стек
    if (stack->top < MAX_STACK_SIZE - 1) { // Проверка, не превышен ли размер стека
        strcpy(stack->items[++stack->top], value); // Копируем строку в стек
    }
}

/**
 * Функция popString извлекает строку из стека, если он не пуст, в противном случае возвращает NULL.
 * 
 * @param stack Параметр `stack` является указателем на структуру `StringStack`. Эта структура вероятно
 * содержит массив строк (`items`), целое число, представляющее индекс верхушки стека (`top`),
 * и, возможно, другие члены, связанные с управлением стеком строк. Функция `popString` 
 * 
 * @return Функция `popString` возвращает верхнюю строку из стека и уменьшает индекс верхушки
 * стека. Если стек пуст, возвращает `NULL`.
 */
char *popString(StringStack *stack) { // Функция для извлечения строки из стека
    if (stack->top >= 0) { // Проверка, не пуст ли стек
        return stack->items[stack->top--]; // Возвращаем верхнюю строку и уменьшаем верхушку стека
    }
    return NULL; // Если стек пуст, возвращаем NULL
}